//==================================Escape sequences==========
package day04javaBasics;

public class escapeseq {
    public static void main(String[] args) {
        System.out.println("Hello\nSuhani");    //\n
        System.out.println("c: Windows\\..");  //\\
        System.out.println("hello\t suhani");  //\t
        System.out.println("she said: \"hello\""); //"\"\""
    }
     
}
