//========================casting========================

//==================implicit casting=====================

package day07javaBasics.casting;

public class implicit {
     
    public static void main(String[] args) {
        // short converted to int 
        short x = 10;
        int y = x + 5;
        System.out.println(y);
        //int converted to double 
        double a = 1.1;
        double b = a + 2;
        System.out.println(b);
    }
}


